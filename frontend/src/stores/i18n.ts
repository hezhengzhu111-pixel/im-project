type MessageMap = (typeof messages)["en-US"];
export type I18nKey = keyof MessageMap;

const locale = ref<Locale>(readLocale());

const applyLocale = (nextLocale: Locale) => {
  if (typeof document !== "undefined") {
    document.documentElement.lang = nextLocale;
  }
  if (typeof localStorage !== "undefined") {
    localStorage.setItem(STORAGE_KEY, nextLocale);
  }
};

applyLocale(locale.value);

const interpolate = (
  template: string,
  vars?: Record<string, string | number>,
) => {
  if (!vars) {
    return template;
  }
  return Object.entries(vars).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  );
};

export const useI18nStore = () => {
  const t = (key: I18nKey, vars?: Record<string, string | number>) => {
    const dictionary = messages[locale.value] as Record<string, string>;
    const fallback = messages["en-US"] as Record<string, string>;
    return interpolate(dictionary[key] || fallback[key] || key, vars);
  };

  const setLocale = (nextLocale: Locale) => {
    locale.value = nextLocale;
    applyLocale(nextLocale);
  };

  const localeOptions = computed(() => [
    { label: "简体中文", value: "zh-CN" as Locale },
    { label: "English", value: "en-US" as Locale },
  ]);

  const localeName = computed(() =>
    locale.value === "zh-CN" ? "简体中文" : "English",
  );

  return {
    locale,
    localeName,
    localeOptions,
    setLocale,
    t,
  };
};
